using UnityEngine;

namespace Animatext.Effects
{
    [CreateAssetMenu(menuName = "Animatext Preset/Transition - Range/Custom/Custom - A05", fileName = "New TRCustomA05 Preset", order = 369)]
    public sealed class TRCustomA05 : DefaultTemplateEffect
    {
        public float singleTime = 1;
        [FadeMode] public ColorMode fadeMode = ColorMode.Multiply;
        public AnimationCurve fadeCurve = AnimationCurve.Linear(0, 0, 1, 1);

        public override InfoFlags infoFlags
        {
            get { return InfoFlags.Range; }
        }

        protected override int unitCount
        {
            get { return 1; }
        }

        protected override float unitTime
        {
            get { return singleTime; }
        }

        protected override void Animate()
        {
            float progress = GetCurrentProgress(0);

            progress = fadeCurve.Evaluate(progress);

            range.Opacify(progress, fadeMode);
        }
    }
}